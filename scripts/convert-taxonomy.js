#!/usr/bin/env node

/**
 * Converts the taxonomy CSV into two data files:
 * 1. src/data/taxonomy-index.ts  – tree structure (categories → subcategories → premade names)
 * 2. public/data/taxonomy-details.json – flat map with descriptions, keywords, type
 */

const fs = require('fs');
const path = require('path');

const CSV_PATH = path.join(__dirname, '..', 'docs', 'NewTaxonomy (Feb 26th, 2026).csv');
const INDEX_PATH = path.join(__dirname, '..', 'src', 'data', 'taxonomy-index.ts');
const DETAILS_PATH = path.join(__dirname, '..', 'public', 'data', 'taxonomy-details.json');

// ── Simple CSV parser that handles quoted fields with commas ──
function parseCSVLine(line) {
  const fields = [];
  let current = '';
  let inQuotes = false;

  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (ch === '"') {
      if (inQuotes && i + 1 < line.length && line[i + 1] === '"') {
        current += '"';
        i++; // skip escaped quote
      } else {
        inQuotes = !inQuotes;
      }
    } else if (ch === ',' && !inQuotes) {
      fields.push(current.trim());
      current = '';
    } else {
      current += ch;
    }
  }
  fields.push(current.trim());
  return fields;
}

// ── Main ──
const raw = fs.readFileSync(CSV_PATH, 'utf-8');
const lines = raw.split(/\r?\n/).filter((l) => l.trim());

// Skip header
const header = parseCSVLine(lines[0]);
console.log('Header:', header);

// Build data structures
// Index: { [category]: { [subCategory]: string[] } }
const index = {};
// Details: { "Category|SubCategory|Premade": { description, keywords, type } }
const details = {};

let lastCategory = '';

for (let i = 1; i < lines.length; i++) {
  const fields = parseCSVLine(lines[i]);
  if (fields.length < 6) continue;

  let [category, subCategory, premade, description, keywords, type] = fields;

  // Inherit category from previous row when blank
  if (category) {
    lastCategory = category;
  } else {
    category = lastCategory;
  }

  if (!category || !subCategory || !premade) continue;

  // Build index tree
  if (!index[category]) index[category] = {};
  if (!index[category][subCategory]) index[category][subCategory] = [];
  index[category][subCategory].push(premade);

  // Build details map
  const key = `${category}|${subCategory}|${premade}`;
  details[key] = {
    description: description || '',
    keywords: keywords || '',
    type: type || '',
  };
}

// ── Write taxonomy-index.ts ──
const categoryCount = Object.keys(index).length;
let subCategoryCount = 0;
let premadeCount = 0;

for (const cat of Object.values(index)) {
  subCategoryCount += Object.keys(cat).length;
  for (const premades of Object.values(cat)) {
    premadeCount += premades.length;
  }
}

const tsContent = `// Auto-generated by scripts/convert-taxonomy.js — DO NOT EDIT
// ${categoryCount} categories, ${subCategoryCount} subcategories, ${premadeCount} premades

export interface TaxonomyIndex {
  [category: string]: {
    [subCategory: string]: string[];
  };
}

export const taxonomyIndex: TaxonomyIndex = ${JSON.stringify(index, null, 2)};
`;

fs.writeFileSync(INDEX_PATH, tsContent, 'utf-8');
console.log(`✓ Wrote ${INDEX_PATH}`);
console.log(`  ${categoryCount} categories, ${subCategoryCount} subcategories, ${premadeCount} premades`);

// ── Write taxonomy-details.json ──
fs.writeFileSync(DETAILS_PATH, JSON.stringify(details), 'utf-8');
const detailsSizeMB = (fs.statSync(DETAILS_PATH).size / 1024 / 1024).toFixed(1);
console.log(`✓ Wrote ${DETAILS_PATH} (${detailsSizeMB} MB)`);
